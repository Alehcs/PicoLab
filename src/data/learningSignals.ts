export const getSignalDefinition = (signalId: string) =>
  learningSignalDefinitions.find((signal) => signal.id === signalId);

export const getSignalsByCategory = (category: LearningSignalCategory) =>
  learningSignalDefinitions.filter((signal) => signal.category === category);

export const getPracticeForSignal = (signalId: string) =>
  getSignalDefinition(signalId)?.suggestedPractice ?? [];

export const getVisualTemplateForSignal = (signalId: string) =>
  getSignalDefinition(signalId)?.suggestedVisualTemplate;

export const groupSignalsByCategory = (signals: LearningSignalInstance[]) =>
  signals.reduce<Record<LearningSignalCategory, LearningSignalInstance[]>>(
    (groups, signal) => {
      groups[signal.category].push(signal);
      return groups;
    },
    {
      algebra: [],
      units: [],
      formula: [],
      concept: [],
      graph: [],
      reading: [],
      calculation: [],
    },
  );

const severityRank = {
  high: 3,
  medium: 2,
  low: 1,
};

const categoryRank: Record<LearningSignalCategory, number> = {
  concept: 7,
  formula: 6,
  units: 5,
  algebra: 4,
  graph: 3,
  reading: 2,
  calculation: 1,
};

export const prioritizeSignals = (signals: LearningSignalInstance[]) =>
  [...signals].sort((a, b) => {
    const severityDelta = severityRank[b.severity] - severityRank[a.severity];
    if (severityDelta) return severityDelta;

    const confidenceDelta = b.confidence - a.confidence;
    if (confidenceDelta) return confidenceDelta;

    const categoryDelta = categoryRank[b.category] - categoryRank[a.category];
    if (categoryDelta) return categoryDelta;

    return new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime();
  });
